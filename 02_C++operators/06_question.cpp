#include<iostream>
using namespace std;

int main () {

    int num1, num2;

    cout << "The first number is: ";
    cin >> num1;

    cout << "The second number is: ";
    cin >> num2;

    cout << "Addition of two given number is: " << num1 + num2 << endl;
    cout << "Substraction of two given number is: " << num1 - num2 << endl;
    cout << "Multiplication of two given number is: " << num1 * num2 << endl;
    cout << "Division of two given number is: " << num1 / num2 << endl;
    
    return 0;
}